import rgb2hsl from '../src/rgb2hsl';
import test from 'ava';

test('convert rgb to hsl', t => {
  t.deepEqual(rgb2hsl([0, 0, 0]), [0, 0, 0]);
  t.deepEqual(rgb2hsl([0, 0, 127]), [240, 100, 25]);
  t.deepEqual(rgb2hsl([0, 0, 255]), [240, 100, 50]);
  t.deepEqual(rgb2hsl([0, 127, 0]), [120, 100, 25]);
  t.deepEqual(rgb2hsl([0, 127, 127]), [180, 100, 25]);
  t.deepEqual(rgb2hsl([0, 127, 255]), [210, 100, 50]);
  t.deepEqual(rgb2hsl([0, 255, 0]), [120, 100, 50]);
  t.deepEqual(rgb2hsl([0, 255, 127]), [150, 100, 50]);
  t.deepEqual(rgb2hsl([0, 255, 255]), [180, 100, 50]);
  t.deepEqual(rgb2hsl([127, 0, 0]), [0, 100, 25]);
  t.deepEqual(rgb2hsl([127, 0, 127]), [300, 100, 25]);
  t.deepEqual(rgb2hsl([127, 0, 255]), [270, 100, 50]);
  t.deepEqual(rgb2hsl([127, 127, 0]), [60, 100, 25]);
  t.deepEqual(rgb2hsl([127, 127, 127]), [0, 0, 50]);
  t.deepEqual(rgb2hsl([127, 127, 255]), [240, 100, 75]);
  t.deepEqual(rgb2hsl([127, 255, 0]), [90, 100, 50]);
  t.deepEqual(rgb2hsl([127, 255, 127]), [120, 100, 75]);
  t.deepEqual(rgb2hsl([127, 255, 255]), [180, 100, 75]);
  t.deepEqual(rgb2hsl([255, 0, 0]), [0, 100, 50]);
  t.deepEqual(rgb2hsl([255, 0, 127]), [330, 100, 50]);
  t.deepEqual(rgb2hsl([255, 0, 255]), [300, 100, 50]);
  t.deepEqual(rgb2hsl([255, 127, 0]), [30, 100, 50]);
  t.deepEqual(rgb2hsl([255, 127, 127]), [0, 100, 75]);
  t.deepEqual(rgb2hsl([255, 127, 255]), [300, 100, 75]);
  t.deepEqual(rgb2hsl([255, 255, 0]), [60, 100, 50]);
  t.deepEqual(rgb2hsl([255, 255, 127]), [60, 100, 75]);
  t.deepEqual(rgb2hsl([255, 255, 255]), [0, 0, 100]);
});
